def insert_records(sb, records: list[dict], label: str) -> None:
    """冪等性確保：既存のtopicはスキップして新規のみ投入。100件単位でバッチ処理。"""
    if not records:
        print(f"[{label}] データなし、スキップ")
        return

    # 既存のtopicを全件取得
    print(f"[{label}] 既存データを確認中...")
    existing_topics: set[str] = set()
    try:
        result = sb.table("showa_rag").select("topic").eq("gender", records[0]["gender"]).execute()
        existing_topics = {r["topic"] for r in (result.data or [])}
        print(f"[{label}] 既存データ: {len(existing_topics)}件")
    except Exception as e:
        print(f"[{label}] 既存データ確認エラー（無視して続行）: {e}")

    new_records = [r for r in records if r["topic"] not in existing_topics]
    skip_count  = len(records) - len(new_records)
    print(f"[{label}] スキップ: {skip_count}件 / 新規投入: {len(new_records)}件")

    if not new_records:
        print(f"[{label}] すべて既存データのため投入をスキップ")
        return

    batch_size = 100
    total      = len(new_records)
    inserted   = 0
    for i in range(0, total, batch_size):
        batch = new_records[i:i + batch_size]
        try:
            sb.table("showa_rag").insert(batch).execute()
            inserted += len(batch)
            print(f"[{label}] {inserted}/{total}件 投入完了")
        except Exception as e:
            print(f"[{label}] バッチ投入エラー（{i}〜{i+batch_size}件目）: {e}")
            raise

    print(f"[{label}] ✅ 投入完了: {inserted}件")


