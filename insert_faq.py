# ── ジャンル別グループ化 ───────────────────────────────────────────
def get_genres() -> list[str]:
    return sorted(set(item["genre"] for item in FAQ_DATA))


def get_by_genre(genre: str) -> list[dict]:
    return [item for item in FAQ_DATA if item["genre"] == genre]


# ── 投入処理 ──────────────────────────────────────────────────────────
def clear_faq(genre: str | None = None) -> None:
    """指定ジャンル（Noneなら全件）をfaqテーブルから削除する。"""
    if genre:
        supabase.table("faq").delete().eq("genre", genre).execute()
        print(f"削除完了: ジャンル「{genre}」")
    else:
        supabase.table("faq").delete().neq("genre", "__never_match__").execute()
        print("削除完了: 全件")


def insert_faq(items: list[dict], dry_run: bool = False) -> None:
    print(f"\n{'[DRY RUN] ' if dry_run else ''}投入件数: {len(items)} 件")
    if dry_run:
        for item in items:
            print(f"  [{item['genre']}] {item['question'][:45]}")
        return

    ok = ng = 0
    for item in items:
        row = {
            "genre":       item["genre"],
            "question":    item["question"],
            "answer":      item["answer"],
            "answer_type": item.get("answer_type", "text"),
            "options":     item.get("options"),
        }
        try:
            supabase.table("faq").insert(row).execute()
            print(f"  OK [{item['genre']}] {item['question'][:45]}")
            ok += 1
        except Exception as e:
            print(f"  NG [{item['genre']}] {item['question'][:45]} → {e}")
            ng += 1
    print(f"\n結果: 成功 {ok} 件 / 失敗 {ng} 件")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--genre",   help="対象ジャンル（例: 健康）")
    parser.add_argument("--all",     action="store_true", help="全件対象")
    parser.add_argument("--dry-run", action="store_true", help="投入せず件数確認")
    parser.add_argument("--clear",   action="store_true", help="投入前に既存データを削除")
    parser.add_argument("--list",    action="store_true", help="ジャンル別件数を表示")
    args = parser.parse_args()

    if args.list:
        print("\nジャンル別件数:")
        for g in get_genres():
            print(f"  {g}: {len(get_by_genre(g))} 件")
        print(f"  合計: {len(FAQ_DATA)} 件")
        return

    if args.all:
        if args.clear and not args.dry_run:
            clear_faq(None)
        insert_faq(FAQ_DATA, dry_run=args.dry_run)
    elif args.genre:
        items = get_by_genre(args.genre)
        if not items:
            print(f"ジャンル '{args.genre}' のデータが見つかりません。")
            print(f"利用可能: {', '.join(get_genres())}")
            sys.exit(1)
        if args.clear and not args.dry_run:
            clear_faq(args.genre)
        insert_faq(items, dry_run=args.dry_run)
    else:
        parser.print_help()


if __name__ == "__main__":
    main()
