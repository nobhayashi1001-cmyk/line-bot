# ── ジャンル別グループ化 ───────────────────────────────────────────
def get_genres() -> list[str]:
    return sorted(set(item["genre"] for item in FAQ_DATA))


def get_by_genre(genre: str) -> list[dict]:
    return [item for item in FAQ_DATA if item["genre"] == genre]


# ── 投入処理 ──────────────────────────────────────────────────────────
def insert_faq(items: list[dict], dry_run: bool = False) -> None:
    print(f"\n{'[DRY RUN] ' if dry_run else ''}投入件数: {len(items)} 件")
    if dry_run:
        for item in items:
            print(f"  [{item['genre']}] {item['question'][:40]}")
        return

    for item in items:
        row = {
            "genre":       item["genre"],
            "question":    item["question"],
            "answer":      item["answer"],
            "answer_type": item.get("answer_type", "text"),
            "options":     item.get("options"),
        }
        try:
            supabase.table("faq").insert(row).execute()
            print(f"  OK [{item['genre']}] {item['question'][:40]}")
        except Exception as e:
            print(f"  NG [{item['genre']}] {item['question'][:40]} → {e}")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--genre", help="投入するジャンルを指定（例: 健康）")
    parser.add_argument("--all",     action="store_true", help="全件投入")
    parser.add_argument("--dry-run", action="store_true", help="実際には投入しない")
    parser.add_argument("--list",    action="store_true", help="ジャンル一覧を表示")
    args = parser.parse_args()

    if args.list:
        for g in get_genres():
            print(f"  {g}: {len(get_by_genre(g))} 件")
        return

    if args.all:
        insert_faq(FAQ_DATA, dry_run=args.dry_run)
    elif args.genre:
        items = get_by_genre(args.genre)
        if not items:
            print(f"ジャンル '{args.genre}' のデータが見つかりません。")
            print(f"利用可能なジャンル: {', '.join(get_genres())}")
            sys.exit(1)
        insert_faq(items, dry_run=args.dry_run)
    else:
        parser.print_help()


if __name__ == "__main__":
    main()
